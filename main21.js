// 8. Write a script that prompt the user to enter side a, side b, and side c of the triangle and and calculate the perimeter of triangle (perimeter = a + b + c)
//    Enter side a: 5
//    Enter side b: 4
//    Enter side c: 3
//    The perimeter of the triangle is 12

const a = parseInt(prompt(`Enter side A`));
const b = parseInt(prompt(`Enter side B`));
const c = parseInt(prompt(`Enter side C`));
parameter = a + b + c ;
alert (`The perimeter of the triangle is ${parameter}`);
